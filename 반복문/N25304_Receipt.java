import java.util.Scanner;

public class Main {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int total = sc.nextInt();
		int kind = sc.nextInt();
		int sum = 0;
		
		for(int i=0; i<kind; i++) {
			int A = sc.nextInt();
			int B = sc.nextInt();
			sum += A*B;
		}

		if(sum==total) {
			System.out.println("Yes");
		} else {
			System.out.println("No");
		}
		
	}

}
